package org.embergraph.bop.engine;

/*
* A message sent to the {@link IQueryClient} when an operator begins executing for some chunk of
 * inputs (an operator on a node against a shard for some binding set chunks generated by downstream
 * operator(s)).
 *
 * @author <a href="mailto:thompsonbry@users.sourceforge.net">Bryan Thompson</a>
 * @version $Id$
 */
public interface IStartOpMessage extends IOpLifeCycleMessage {

  /*
   * The #of {@link IChunkMessage} accepted as the input for the operator.
   *
   * <p>Note: This should be ONE (1) unless {@link IChunkMessage} are being combined for the
   * operator.
   */
  int getChunkMessageCount();
}
